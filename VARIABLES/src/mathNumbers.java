
public class mathNumbers {
    public static void main(String []args) {
    	
    	System.out.println("3+4 " +(3+4));
    	System.out.println("3-4 "+(3-4));
    	System.out.println("3/5 "+(3/5)+" "+(5/3));
    	System.out.println("3%5 "+(3%5)+" "+(5%3));
    	System.out.println("6%8 "+(6%8)+" "+(6%8));
    	System.out.println("800451%800452 "+(800451%800452)+" "+(800451%800452));
    	System.out.println(3+4);
    	System.out.println(3-4);
    	System.out.println(3/5);
    	System.out.println(3%5);
    	System.out.println(4.0/3.0);
    	int a=4;
    	double b=4.5;
    	System.out.println(5+a);
    	System.out.println(a+b);//double + int ==double
    	System.out.println(Math.abs(-8));
    	System.out.println(Math.abs(-8.5));
    	System.out.println(Math.pow(3, a));//power of integer gives decimal
    	System.out.println((int)Math.pow(3,4)); // direct int  gives decimal op
    	System.out.println(Math.sqrt(a));//sqrt(integer) gives decimal
    	System.out.println(Math.sqrt(4));//direct int gives decimal op
    	System.out.println(Math.min(a, b));//given int variable gives  decimal op
    	System.out.println(Math.min(4,3));//direct int  gives integer as op
    	System.out.println(Math.round(2.5));
    	System.out.println(Math.round(b));
    	System.out.println(Math.random());
    	
    	
    	
    }
}
