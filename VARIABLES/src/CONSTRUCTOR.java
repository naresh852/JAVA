
public class CONSTRUCTOR {
    String name;
    String author;
    int pages;
    String language;
    int publisheddate;
    //THIS IS A CONSTRUCTOR,CONSTRUCTOR IS A FUNCTION IN CLASS
    public CONSTRUCTOR(String a,String b,int c,String d,int e) {
    	this.name=a;
    	this.author=b;
    	this.pages=c;
    	this.language=d;
    	this.publisheddate=e;
    	
    }
    
}
